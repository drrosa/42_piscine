int	ft_putchar(char c);

int	main(int argc, char **argv)
{
	int i;
	int j;

	i = argc - 1;
	j = 0;
	while (i > 0)
	{
		while (argv[i][j])
			ft_putchar(argv[i][j++]);
		j = 0;
		i--;
		ft_putchar('\n');
	}
	return (0);
}
