int	ft_str_is_printable(char *str)
{
	int	i;
	int result;

	i = 0;
	result = 1;
	while (str[i])
	{
		if (str[i] < ' ' || str[i] > '~')
			result = 0;
		i++;
	}
	return (result);
}
